function randomizeLuckyIDs(inputId) {
    const inputElement = document.getElementById(inputId);
    if (!inputElement) return;

    const min = 1;
    const max = 99;
    const count = 10;
    const numbers = new Set();
    while(numbers.size < count) {
        numbers.add(Math.floor(Math.random() * (max - min + 1)) + min);
    }
    
    const luckyString = Array.from(numbers).sort((a, b) => a - b).join(' | ');
    inputElement.value = luckyString;

    updatePreview(); 
}

