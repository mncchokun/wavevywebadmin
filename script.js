function updatePreview() {
    if (!currentTemplateName) return;
    const templateData = ANNOUNCEMENT_DATA.backups.find(t => t.name === currentTemplateName);
    if (!templateData) return;
    
    const safeTemplateName = makeSafeId(currentTemplateName);
    const form = document.getElementById(`form-${safeTemplateName}`);
    if (!form) return;

    const formData = new FormData(form);
    const data = Object.fromEntries(formData.entries());

    // Fix Checkbox values for Preview
    const luckyToggle = document.getElementById(`${safeTemplateName}-toggle_lucky`);
    if (luckyToggle) data.toggle_lucky = luckyToggle.checked ? 'on' : 'off';
    
    const commToggle = document.getElementById(`${safeTemplateName}-toggle_community_service`);
    if (commToggle) data.toggle_community_service = commToggle.checked ? 'on' : 'off';

    const payload = generatePayload(templateData, data);
    const previewContainer = document.getElementById('discord-preview-container');
    if (!previewContainer) return;
    
    previewContainer.innerHTML = '';

    if (payload.embeds && payload.embeds.length > 0) {
        payload.embeds.forEach(embed => {
            const cleanText = (text) => {
                if (!text) return '';
                return text.replace(/<a?:\w+:\d+>|‎/g, '')
                           .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')
                           .replace(/#+ (.*)/g, '<h4>$1</h4>')
                           .replace(/```(.*?)```/gs, '<pre style="background:#2f3136; padding:5px; border-radius:3px; color:#b9bbbe; overflow-x:auto;">$1</pre>');
            };

            const embedHtml = `
                <div class="discord-embed" style="border-left-color: ${decToHex(embed.color || 0)};">
                    ${embed.title ? `<div class="embed-title">${cleanText(embed.title)}</div>` : ''}
                    ${embed.description ? `<div class="embed-description">${cleanText(embed.description).replace(/\n/g, '<br>')}</div>` : ''}
                    ${embed.image && embed.image.url ? `<img src="${embed.image.url}" class="embed-image" alt="Image">` : ''}
                </div>
            `;
            previewContainer.innerHTML += embedHtml;
        });
    }
}

function generatePayload(templateData, formData) {
    let payload = JSON.parse(JSON.stringify(templateData.messages[0].data));
    
    // Lucky ID Logic
    if (templateData.name === 'WAVEVY-OPENSERVER') {
        const showLucky = formData.toggle_lucky === 'on'; 
        payload.embeds = payload.embeds.filter(embed => {
            return embed.custom_field === 'lucky_embed' ? showLucky : true;
        });
    }

    // Community Service Logic
    let communityText = "";
    if (formData.toggle_community_service === 'on' && formData.community_service) {
        communityText = `บำเพ็ญประโยชน์ : ${formData.community_service} ไม้`;
    }

    const replacements = {
        '[name]': formData.name || 'N/A',
        '[discord_id]': formData.discord_id || '000000000000000000',
        '[steam_hex]': formData.steam_hex || 'N/A',
        '[offense]': formData.offense || 'N/A',
        '[fine_amount]': formData.fine_amount ? parseInt(formData.fine_amount).toLocaleString() : '0',
        '[card_type]': formData.card_type || 'ใบเหลือง',
        '[community_service]': communityText,
        '[due_date]': formData.due_date || 'N/A',
        '[due_time]': formData.due_time || 'N/A',
        '[lucky_ids]': formData.lucky_ids || '...'
    };
    
    if (payload.embeds) {
        payload.embeds.forEach(embed => {
            if (embed.description) {
                for (const [key, value] of Object.entries(replacements)) {
                    embed.description = embed.description.split(key).join(value);
                }
            }
        });
    }

    return payload;
}

async function handleFormSubmission(event) {
    event.preventDefault();
    
    // 1. แสดง Loading Overlay
    const overlay = document.getElementById('loadingOverlay');
    if (overlay) overlay.classList.remove('d-none');

    const templateName = event.target.getAttribute('data-template-name');
    const templateData = ANNOUNCEMENT_DATA.backups.find(t => t.name === templateName);
    const formData = new FormData(event.target);
    const data = Object.fromEntries(formData.entries());

    // Explicitly check checkboxes for Submission
    const safeName = makeSafeId(templateName);
    const luckyToggle = document.getElementById(`${safeName}-toggle_lucky`);
    if (luckyToggle) data.toggle_lucky = luckyToggle.checked ? 'on' : 'off';
    
    const commToggle = document.getElementById(`${safeName}-toggle_community_service`);
    if (commToggle) data.toggle_community_service = commToggle.checked ? 'on' : 'off';

    const payload = generatePayload(templateData, data);
    const webhookUrls = templateData.targets.map(t => t.url);

    try {
        for (const url of webhookUrls) {
            await fetch(url, {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify(payload)
            });
        }
        // 2. ซ่อน Loading เมื่อสำเร็จ
        if (overlay) overlay.classList.add('d-none');
        alert('✅ ส่งประกาศสำเร็จ!');
    } catch (err) {
        // 3. ซ่อน Loading เมื่อเกิดข้อผิดพลาด
        if (overlay) overlay.classList.add('d-none');
        alert('❌ เกิดข้อผิดพลาดในการส่ง');
    }
}

